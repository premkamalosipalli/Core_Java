package com.loops;

public class Loops13 {

	public static void main(String[] args) {

		int number=25;
		
		if(number<=10) {
			System.out.println("Largest number in 10");
		}else if(number>10&&number<=20) {
			System.out.println("Largest number in 20");
		}else if(number>20&&number<=30) {
			System.out.println("Largest number in 30");
		}
	}

}
