package com.loops;

public class Loops12 {

	public static void main(String[] args) {

		char gender='M';
		
		switch (gender) {
		case 'M':
			
			System.out.println("Male");
			break;
			
		case 'F':
			
			System.out.println("Male");
			break;
			
		default:
			System.out.println("Give a Valid Gender");
			break;
		}
	}

}
